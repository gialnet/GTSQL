CREATE OR REPLACE PROCEDURE CHECK_EXIT_IBI_FINCA(
              xYEAR 	  IN	 CHAR,
              xMUNICIPIO  IN	 CHAR,
              xRESP 	  OUT	 INTEGER
)
AS
xTEMP INTEGER;
BEGIN

	xRESP:=0;

	SELECT COUNT(*) INTO xTEMP
	  FROM IBI_FINCA
	 WHERE MUNICIPIO=xMUNICIPIO AND YEAR=xYEAR;

	IF xTEMP>0 THEN
	      xRESP:=1;
	END IF;

END;
