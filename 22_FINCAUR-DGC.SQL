CREATE OR REPLACE PROCEDURE BORRA_VIEJO_IBI_FINCA
		(xYEAR 	CHAR, 
		xMUNICIPIO 	CHAR)
AS
BEGIN

  DELETE FROM IBI_REPARTO_CARGOS WHERE MUNICIPIO=xMUNICIPIO AND YEAR=xYEAR;

  DELETE FROM IBI_REPARTO_LOCALES WHERE MUNICIPIO=xMUNICIPIO AND YEAR=xYEAR;

  DELETE FROM IBI_CARGO WHERE MUNICIPIO=xMUNICIPIO AND YEAR=xYEAR;

  DELETE FROM IBI_LOCALES WHERE MUNICIPIO=xMUNICIPIO AND YEAR=xYEAR;

  DELETE FROM IBI_FINCA WHERE MUNICIPIO=xMUNICIPIO AND YEAR=xYEAR;

END;
